                fi
                ;;
            6)
                log "INFO" "User selected: Container Cleanup"
                cleanup_containers
                ;;
            7)
                log "INFO" "User selected: Snapshot Setup"
                setup_system_snapshots
                ;;
            8)
                log "INFO" "User selected: System Health Report"
                generate_system_health_report
                ;;
            9)
